package com.auto.action.pojo;

public class XpathPojo {

    private String xpathKey;

    private String  xpathValue;

    private String  xpathType;


    public String getXpathKey() {
        return xpathKey;
    }

    public void setXpathKey(String xpathKey) {
        this.xpathKey = xpathKey;
    }

    public String getXpathValue() {
        return xpathValue;
    }

    public void setXpathValue(String xpathValue) {
        this.xpathValue = xpathValue;
    }

    public String getXpathType() {
        return xpathType;
    }

    public void setXpathType(String xpathType) {
        this.xpathType = xpathType;
    }
}
